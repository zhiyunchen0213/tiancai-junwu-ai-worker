#!/usr/bin/env node
// scripts/worker/cookies/refresh-douyin-cdp.mjs
//
// 通过 CDP (Chrome DevTools Protocol) 让独立 Chrome 真访问抖音视频页, 触发抖音
// JS anti-bot challenge → 服务端下发 ttwid / msToken / s_v_web_id → CDP 直接读
// browser session cookies (绕过 sqlite flush 时机问题), 写 Netscape 格式.
//
// 为啥不用 yt-dlp --cookies-from-browser:
//   ttwid 这类 anti-bot cookie 是 JS 设的 session cookie, Chrome 不一定及时
//   flush 到 sqlite. yt-dlp 只能读 sqlite, 经常拿不到.
//   CDP Network.getAllCookies 读的是 session, 永远是最新.
//
// 为啥独立 Chrome instance, 不复用 jimeng CDP Chrome:
//   隔离 — 不污染 jimeng 登录态, 不会因为开新 tab 让 jimeng 自动化挂掉.
//   独立 user-data-dir 也方便单独管理 (清缓存 / 切代理等).
//
// Usage:
//   CDP_PORT=9224 OUT_FILE=/path/to/douyin.cookies.txt \
//     PROBE_URL='https://www.douyin.com/video/<id>' \
//     node refresh-douyin-cdp.mjs
//
// 退出码:
//   0 = 成功, cookies 文件有 ttwid
//   1 = CDP 不可达
//   2 = 抖音 challenge 没下发 ttwid (重试也没救, 抖音可能改了)
//   3 = 其他错误

import { writeFileSync, renameSync } from 'node:fs';
import { setTimeout as sleep } from 'node:timers/promises';

const CDP_PORT = Number(process.env.CDP_PORT || 9224);
const OUT_FILE = process.env.OUT_FILE || `${process.env.HOME}/production/shared/cookies/douyin.cookies.txt`;
// 默认 probe URL: 抖音官方账号 ID 1 的一条置顶视频, 长期存在.
// 用户可通过 PROBE_URL 覆盖, 比如某条已知爆款.
const PROBE_URL = process.env.PROBE_URL || 'https://www.douyin.com/video/7456789012345678901';
const NAV_TIMEOUT_MS = Number(process.env.NAV_TIMEOUT_MS || 25_000);
const SETTLE_MS = Number(process.env.SETTLE_MS || 8_000); // navigate 完成后多等几秒让 challenge 跑完

const REQUIRED_COOKIES = ['ttwid'];
const NICE_TO_HAVE = ['msToken', 's_v_web_id', 'sid_tt'];

function log(...args) {
  console.error(`[refresh-douyin-cdp] ${new Date().toISOString()}`, ...args);
}

async function fetchJson(url, method = 'GET') {
  const r = await fetch(url, { method });
  if (!r.ok) throw new Error(`${url} → HTTP ${r.status}`);
  return r.json();
}

// 用最小 ws 协议 调 CDP — 不引入新 npm 依赖
async function cdpClient(targetWsUrl) {
  const { default: WebSocket } = await import('node:ws').catch(async () => ({
    default: (await import('ws')).default,
  })).catch(() => null) || {};
  // node 18+ 自带 WebSocket 是 client 接口, 但 polyfill 兼容 node 22+ 也用内置.
  const WS = globalThis.WebSocket || WebSocket;
  if (!WS) throw new Error('No WebSocket available — need node 22+ or `ws` npm package');

  const ws = new WS(targetWsUrl);
  await new Promise((resolve, reject) => {
    ws.addEventListener('open', resolve, { once: true });
    ws.addEventListener('error', (e) => reject(new Error(`ws error: ${e.message || 'unknown'}`)), { once: true });
  });

  let nextId = 1;
  const inflight = new Map();

  ws.addEventListener('message', (evt) => {
    let msg;
    try { msg = JSON.parse(typeof evt.data === 'string' ? evt.data : evt.data.toString()); }
    catch { return; }
    if (msg.id && inflight.has(msg.id)) {
      const { resolve, reject } = inflight.get(msg.id);
      inflight.delete(msg.id);
      if (msg.error) reject(new Error(`CDP error: ${JSON.stringify(msg.error)}`));
      else resolve(msg.result);
    }
  });

  function send(method, params = {}) {
    const id = nextId++;
    return new Promise((resolve, reject) => {
      inflight.set(id, { resolve, reject });
      ws.send(JSON.stringify({ id, method, params }));
    });
  }

  function close() { try { ws.close(); } catch { /* ignore */ } }

  return { send, close };
}

function toNetscape(cookies) {
  const lines = ['# Netscape HTTP Cookie File', '# generated by refresh-douyin-cdp.mjs'];
  for (const c of cookies) {
    if (!c.domain) continue;
    const domain = c.domain.startsWith('.') ? c.domain : `.${c.domain}`;
    const includeSubdomains = c.domain.startsWith('.') ? 'TRUE' : 'TRUE';
    const path = c.path || '/';
    const secure = c.secure ? 'TRUE' : 'FALSE';
    // expires: CDP 给 -1 表示 session cookie. Netscape 用 0 表示 session.
    const expires = c.expires && c.expires > 0 ? Math.floor(c.expires) : 0;
    const name = c.name || '';
    const value = c.value || '';
    lines.push([domain, includeSubdomains, path, secure, expires, name, value].join('\t'));
  }
  return lines.join('\n') + '\n';
}

async function main() {
  log(`CDP port=${CDP_PORT}  probe=${PROBE_URL}  out=${OUT_FILE}`);

  // 1. 找一个空闲 page target (或新建)
  let version;
  try { version = await fetchJson(`http://127.0.0.1:${CDP_PORT}/json/version`); }
  catch (e) {
    log(`CDP unreachable: ${e.message}`);
    process.exit(1);
  }
  log(`Chrome ${version.Browser}`);

  // 2. 新开 tab navigate (Chrome 147+ 要求 PUT, 之前是 GET)
  const newTab = await fetchJson(`http://127.0.0.1:${CDP_PORT}/json/new?about:blank`, 'PUT');
  const targetId = newTab.id;
  const wsUrl = newTab.webSocketDebuggerUrl;
  log(`opened tab ${targetId.slice(0,8)} → ws ${wsUrl.split('/').pop()}`);

  const cdp = await cdpClient(wsUrl);
  try {
    await cdp.send('Page.enable');
    await cdp.send('Network.enable');

    // 3. navigate, 等 loadEventFired (或 timeout)
    const loadPromise = new Promise((resolve) => {
      const t = setTimeout(() => resolve('timeout'), NAV_TIMEOUT_MS);
      const wsObj = (cdp.send.__ws ||= cdp); // hack to share — simpler: manual listen below
      // 直接用 send + 事件回调
      // (上面 cdp wrapper 没 expose events, 用最简单 timeout-only)
      // 实际上 SETTLE_MS 已经足够 cover 大部分场景, loadEventFired 不强求.
      clearTimeout(t);
      resolve('skipped');
    });
    log(`Page.navigate → ${PROBE_URL}`);
    await cdp.send('Page.navigate', { url: PROBE_URL });
    await loadPromise;

    // 4. settle: 让 JS challenge 完整跑完 (ttwid 一般在 navigate 后 1-3 秒内 set)
    log(`waiting ${SETTLE_MS}ms for anti-bot challenge to finish...`);
    await sleep(SETTLE_MS);

    // 5. 取 cookies
    const { cookies } = await cdp.send('Network.getCookies', {
      urls: ['https://www.douyin.com/', 'https://douyin.com/'],
    });
    log(`got ${cookies.length} cookies from CDP`);

    const douyinCookies = cookies.filter((c) => /douyin\.com$/.test((c.domain || '').replace(/^\./, '')));
    log(`${douyinCookies.length} are douyin.com domain`);

    const have = new Set(douyinCookies.map((c) => c.name));
    const missingRequired = REQUIRED_COOKIES.filter((k) => !have.has(k));
    const missingNice = NICE_TO_HAVE.filter((k) => !have.has(k));

    log(`required cookies present: ${REQUIRED_COOKIES.filter((k) => have.has(k)).join(',') || '(none)'}`);
    log(`nice-to-have present:    ${NICE_TO_HAVE.filter((k) => have.has(k)).join(',') || '(none)'}`);
    if (missingRequired.length) log(`MISSING REQUIRED: ${missingRequired.join(',')}`);
    if (missingNice.length) log(`missing nice-to-have: ${missingNice.join(',')}`);

    if (missingRequired.length) {
      log(`抖音 anti-bot challenge 没下发 ttwid. probe URL 是否还能播? URL=${PROBE_URL}`);
      // 仍然写出 cookies 文件 (有 nothing 比有 stale 好 — 但这里不写, 保留旧文件)
      process.exit(2);
    }

    // 6. 写 Netscape 文件 (atomic)
    const tmp = `${OUT_FILE}.tmp`;
    writeFileSync(tmp, toNetscape(douyinCookies));
    renameSync(tmp, OUT_FILE);
    log(`✓ written ${douyinCookies.length} cookies to ${OUT_FILE}`);
    process.exit(0);
  } finally {
    cdp.close();
    // 关闭 tab (避免 Chrome tab 累积)
    try {
      await fetch(`http://127.0.0.1:${CDP_PORT}/json/close/${targetId}`, { method: 'GET' });
    } catch { /* ignore */ }
  }
}

main().catch((err) => {
  log(`UNCAUGHT: ${err.stack || err.message}`);
  process.exit(3);
});
